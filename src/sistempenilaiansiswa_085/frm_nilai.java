    private String getIndex(int nilai) {
        if (nilai >= 90 && nilai <= 100) {
            return "A";
        } else if (nilai >= 70 && nilai < 90) {
            return "B";
        } else if (nilai >= 50 && nilai < 70) {
            return "C";
        } else if (nilai >= 30 && nilai < 50) {
            return "D";
        } else {
            return "E";
        }
    }
    private String getKet(int nilai) {
        if (nilai >= 90 && nilai <= 100) {
            return "LULUS";
        } else if (nilai >= 70 && nilai < 90) {
            return "LULUS";
        } else if (nilai >= 50 && nilai < 70) {
            return "LULUS";
        } else if (nilai >= 30 && nilai < 50) {
            return "LULUS";
        } else {
            return "Tidak LULUS";
        }
    }
    
    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        // TODO add your handling code here:
    String kd_nilai = txt_kd_nilai.getText();
    String nim = txt_nim.getText();
    String kd_mk = txt_kd_mk.getText();
    String nilai = txt_nilai.getText();
    String ket = txt_ket.getText();

    if (kd_nilai.isEmpty() || nim.isEmpty() || kd_mk.isEmpty() || nilai.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Data tidak boleh kosong, silahkan dilengkapi");
        txt_kd_nilai.requestFocus();
    } else {
        try {
            Class.forName(driver);
            try (Connection kon = DriverManager.getConnection(database, user, pass);
                 Statement stt = kon.createStatement()) {
                
                int nilaiInt = Integer.parseInt(nilai);
                String index = getIndex(nilaiInt);

                String SQL = "UPDATE `t_nilai` SET "
                     + "`nim` = '" + nim + "', "
                     + "`kd_mk` = '" + kd_mk + "', "
                     + "`nilai` = '" + nilaiInt + "', "
                     + "`index` = '" + index + "', "
                     + "`ket` = '" + ket + "' "
                     + "WHERE `kd_nilai` = '" + kd_nilai + "';";
                
                stt.executeUpdate(SQL);
                data[0] = kd_nilai;
                data[1] = nim;
                data[2] = kd_mk;
                data[3] = String.valueOf(nilaiInt);
                data[4] = index;
                data[5] = ket;
                tableModel.removeRow(row);
                tableModel.insertRow(row, data);
                stt.close();
                kon.close();
                membersihkan_teks();
                btn_tambah.setEnabled(true);
                btn_ubah.setEnabled(false);
                btn_hapus.setEnabled(false);
                btn_batal.setEnabled(false);
                btn_keluar.setEnabled(true);
                nonaktif_teks();
                nonaktif_cari();
            }
        } catch (Exception ex) {
            System.err.println(ex.getMessage());
            JOptionPane.showMessageDialog(null,
                    ex.getMessage(), "Error",
            JOptionPane.INFORMATION_MESSAGE
            );
            System.exit(0);
        }
    }                              
    }//GEN-LAST:event_btn_ubahActionPerformed

