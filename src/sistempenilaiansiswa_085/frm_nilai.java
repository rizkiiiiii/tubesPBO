    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
    String data[] = new String[6];
    
    if (txt_kd_nilai.getText().isEmpty() || txt_nim.getText().isEmpty() || txt_kd_mk.getText().isEmpty() || txt_nilai.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, 
                    "Data tidak boleh kosong, silahkan dilengkapi");
        txt_kd_nilai.requestFocus();
    } else {
        try {
            Class.forName(driver);
            Connection kon = DriverManager.getConnection(
                             database,
                             user,
                             pass);
            Statement stt = kon.createStatement();
           
            int nilaiInt = Integer.parseInt(txt_nilai.getText());
            String index = getIndex(nilaiInt);
//            String ket = getKet(nilaiInt);
            
            String SQL = "INSERT INTO t_nilai(`kd_nilai`, `nim`, `kd_mk`, `nilai`, `index`, `ket`)"
                        + "VALUES ('"+txt_kd_nilai.getText()+"',"
                        + "'"+txt_nim.getText()+"',"
                        + "'"+txt_kd_mk.getText()+"',"
                        + "'"+nilaiInt+"',"
                        + "'"+index+"',"
                        + "'"+txt_ket.getText()+"')";
            System.out.println("SQL String: " + SQL);
                    
            stt.executeUpdate(SQL);
            data[0] = txt_kd_nilai.getText();
            data[1] = txt_nim.getText();
            data[2] = txt_kd_mk.getText();
            data[3] = String.valueOf(nilaiInt);
            data[4] = String.valueOf(index);
            data[5] = txt_ket.getText();
            tableModel.insertRow(0, data);
            stt.close();
            kon.close();
            membersihkan_teks();
            btn_tambah.setEnabled(true);
            btn_ubah.setEnabled(true);
            btn_hapus.setEnabled(true);
            btn_batal.setEnabled(false);
            btn_simpan.setEnabled(false);
            btn_keluar.setEnabled(true);
            nonaktif_teks();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,
                    ex.getMessage(), "Error",
            JOptionPane.INFORMATION_MESSAGE
            );
        }
    }
    }//GEN-LAST:event_btn_simpanActionPerformed

